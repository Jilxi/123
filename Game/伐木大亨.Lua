        local new = textMap[v]
        if new then v = new end
    elseif k == "PlaceholderText" and t:IsA("TextBox") then
        local new = textMap[v]
        if new then v = new end
    elseif k == "Title" and (t:IsA("TextButton") or t:IsA("Frame")) then
        local new = textMap[v]
        if new then v = new end
    end
    return oldIndex(t, k, v)
end)

